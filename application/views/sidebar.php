<h2>About Sedona, Arizona</h2>
<h3> &quot;The Heart of Red Rock Country&quot; </h3> 
<p>Sedona is one of Arizona's most popular destinations. It is set amongst towering sandstone cliffs that truly glow with an energized red hue. Just north of town is Oak Creek Canyon with its own dramatic beauty, swimming holes, natural rock slides and camping in its cool forest.</p>
<p>The stunning natural beauty of the area combined with its relaxed atmosphere has drawn many artists to the area. Art galleries are numerous, offering many varieties of art in all price ranges.</p>
<p>Sedona is located 120 miles north of Phoenix in Arizona's central mountains. It is 30 miles south of Flagstaff.</p>
